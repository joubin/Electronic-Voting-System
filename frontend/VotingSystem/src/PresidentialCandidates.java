
public class PresidentialCandidates {
	public String party_affiliation;
	public String full_name;
	public int id;
	   
	public void setPartyAffiliation(String s){
		this.party_affiliation = s;
	}
	   
	public String getPartyAffiliation(){
	   return party_affiliation;
	}
	   
	public void setFullName(String s){
	   this.full_name = s;
	}
	   
	public String getFullName(){
	   return full_name;
	}
	   
	public void setID(int i){
	   this.id = i;
	}
	   
	public int getID(){
	   return id;
	}
}
